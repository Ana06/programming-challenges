def brains
  (1..).each do |x|
    next unless x % 2 == 1
    next unless x % 3 == 2
    next unless x % 4 == 3
    next unless x % 5 == 4
    next unless x % 6 == 5
    next unless x % 7 == 0
    return x
  end
end

